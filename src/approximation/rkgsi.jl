                Δms₁₂ =  𝒒̂ᵀ𝗚⁻¹𝒒*Δn₁s₂n₂s₁/2

                Δ∂ms₁₁∂x = ∂𝒒̂ᵀ∂x𝗚⁻¹𝒒*Δn₁s₁
                Δ∂ms₁₁∂y = ∂𝒒̂ᵀ∂y𝗚⁻¹𝒒*Δn₁s₁
                Δ∂ms₂₂∂x = ∂𝒒̂ᵀ∂x𝗚⁻¹𝒒*Δn₂s₂
                Δ∂ms₂₂∂y = ∂𝒒̂ᵀ∂y𝗚⁻¹𝒒*Δn₂s₂
                Δ∂ms₁₂∂x = ∂𝒒̂ᵀ∂x𝗚⁻¹𝒒*Δn₁s₂n₂s₁/2
                Δ∂ms₁₂∂y = ∂𝒒̂ᵀ∂y𝗚⁻¹𝒒*Δn₁s₂n₂s₁/2

                for i in 1:length(𝓒)
                    ∂²𝝭∂x²[i] += 𝝭[i]*Δms₁₁
                    ∂²𝝭∂x∂y[i] += 𝝭[i]*Δms₁₂
                    ∂²𝝭∂y²[i] += 𝝭[i]*Δms₂₂
                    ∂∂²𝝭∂x²∂x[i] += 𝝭[i]*Δ∂ms₁₁∂x
                    ∂∂²𝝭∂x²∂y[i] += 𝝭[i]*Δ∂ms₁₁∂y
                    ∂∂²𝝭∂x∂y∂x[i] += 𝝭[i]*Δ∂ms₁₂∂x
                    ∂∂²𝝭∂x∂y∂y[i] += 𝝭[i]*Δ∂ms₁₂∂y
                    ∂∂²𝝭∂y²∂x[i] += 𝝭[i]*Δ∂ms₂₂∂x
                    ∂∂²𝝭∂y²∂y[i] += 𝝭[i]*Δ∂ms₂₂∂y
                end
            end
        end
    end
end

function set∇̄²𝝭!(ap::ReproducingKernel{𝒑,𝑠,𝜙,:Tri3};Γᵍ::Vector{ReproducingKernel{𝒑,𝑠,𝜙,:Tri3}}=[],Γᶿ::Vector{ReproducingKernel{𝒑,𝑠,𝜙,:Tri3}}=[],Γᴾ::Vector{ReproducingKernel{𝒑,𝑠,𝜙,:Tri3}}=[]) where {𝒑,𝑠,𝜙}
    x₁ = ap.𝓒[1].x;y₁ = ap.𝓒[1].y
    x₂ = ap.𝓒[2].x;y₂ = ap.𝓒[2].y
    x₃ = ap.𝓒[3].x;y₃ = ap.𝓒[3].y
    𝐴 = get𝐴(ap)
    n₁₁ = y₃-y₂;n₂₁ = y₁-y₃;n₃₁ = y₂-y₁
    n₁₂ = x₂-x₃;n₂₂ = x₃-x₁;n₃₂ = x₁-x₂
    s₁₁ = -n₁₂;s₂₁ = -n₂₂;s₃₁ = -n₃₂
    s₁₂ =  n₁₁;s₂₂ =  n₂₁;s₃₂ =  n₃₁
    𝓒 = ap.𝓒
    𝓖 = ap.𝓖
    for ξ̂ in 𝓖
        𝒒̂ = get𝒑₂(ap,ξ̂)
        𝗚⁻¹ = cal𝗚₂!(ap)
        𝒒̂ᵀ𝗚⁻¹ = 𝒒̂*𝗚⁻¹

        ∂²𝝭∂x² = ξ̂[:∂²𝝭∂x²_]
        ∂²𝝭∂x∂y = ξ̂[:∂²𝝭∂x∂y_]
        ∂²𝝭∂y² = ξ̂[:∂²𝝭∂y²_]
        for i in 1:length(𝓒)
            ∂²𝝭∂x²[i] = 0.0
            ∂²𝝭∂x∂y[i] = 0.0
            ∂²𝝭∂y²[i] = 0.0
        end
        for a in Γᵍ
            if ap∩a ≠ nothing
                for ξ in a.𝓖
                    𝑤 = ξ.𝑤
                    n₁ = ξ.n₁
                    n₂ = ξ.n₂
                    s₁ = -n₂
                    s₂ = n₁
                    𝝭 = ξ[:𝝭]
                    𝒒, ∂𝒒∂ξ, ∂𝒒∂η = get∇²𝒑₂(a,ξ)

                    𝒒̂ᵀ𝗚⁻¹∂𝒒∂ξ = 𝒒̂ᵀ𝗚⁻¹*∂𝒒∂ξ
                    𝒒̂ᵀ𝗚⁻¹∂𝒒∂η = 𝒒̂ᵀ𝗚⁻¹*∂𝒒∂η

                    q₁ = 𝒒̂ᵀ𝗚⁻¹∂𝒒∂ξ*n₁₁ + 𝒒̂ᵀ𝗚⁻¹∂𝒒∂η*n₂₁
                    q₂ = 𝒒̂ᵀ𝗚⁻¹∂𝒒∂ξ*n₁₂ + 𝒒̂ᵀ𝗚⁻¹∂𝒒∂η*n₂₂

                    q₁n₁ = q₁*n₁
                    q₁n₂ = q₁*n₂
                    q₂n₁ = q₂*n₁
                    q₂n₂ = q₂*n₂
                    ms₁₁ = (q₁*s₁+q₂*s₂)*n₁*s₁
                    ms₁₂ = (q₁*s₁+q₂*s₂)*0.5*(n₁*s₂+n₂*s₁)
                    ms₂₂ = (q₁*s₁+q₂*s₂)*n₂*s₂

                    W₁₁ = (q₁n₁+ms₁₁)*𝑤/2/𝐴
                    W₁₂ = (q₁n₂+q₂n₁+2*ms₁₂)*𝑤/4/𝐴
                    W₂₂ = (q₂n₂+ms₂₂)*𝑤/2/𝐴
                    for i in 1:length(𝓒)
                        ∂²𝝭∂x²[i] += 𝝭[i]*W₁₁
                        ∂²𝝭∂x∂y[i] += 𝝭[i]*W₁₂
                        ∂²𝝭∂y²[i] += 𝝭[i]*W₂₂
                    end
                end
            end
        end

        for b in Γᶿ
            if ap∩b ≠ nothing
                for ξ in b.𝓖
                    𝑤 = ξ.𝑤
                    n₁ = ξ.n₁
                    n₂ = ξ.n₂
                    s₁ = -n₂
                    s₂ = n₁
                    ∂𝝭∂x = ξ[:∂𝝭∂x]
                    ∂𝝭∂y = ξ[:∂𝝭∂y]
                    𝒒 = get𝒑₂(b,ξ)

                    𝒒̂ᵀ𝗚⁻¹𝒒 =  𝒒̂ᵀ𝗚⁻¹*𝒒

                    mn₁₁n₁ = 𝒒̂ᵀ𝗚⁻¹𝒒*n₁*n₁*n₁
                    mn₁₁n₂ = 𝒒̂ᵀ𝗚⁻¹𝒒*n₁*n₁*n₂
                    mn₁₂n₁ = 𝒒̂ᵀ𝗚⁻¹𝒒*n₁*n₂*n₁
                    mn₁₂n₂ = 𝒒̂ᵀ𝗚⁻¹𝒒*n₁*n₂*n₂
                    mn₂₂n₁ = 𝒒̂ᵀ𝗚⁻¹𝒒*n₂*n₂*n₁
                    mn₂₂n₂ = 𝒒̂ᵀ𝗚⁻¹𝒒*n₂*n₂*n₂

                    W₁₁₁ = mn₁₁n₁*𝑤
                    W₁₁₂ = mn₁₁n₂*𝑤
                    W₁₂₁ = mn₁₂n₁*𝑤
                    W₁₂₂ = mn₁₂n₂*𝑤
                    W₂₂₁ = mn₂₂n₁*𝑤
                    W₂₂₂ = mn₂₂n₂*𝑤

                    for i in 1:length(𝓒)
                        ∂²𝝭∂x²[i] += ∂𝝭∂x[i]*W₁₁₁ + ∂𝝭∂y[i]*W₁₁₂
                        ∂²𝝭∂x∂y[i] += ∂𝝭∂x[i]*W₁₂₁ + ∂𝝭∂y[i]*W₁₂₂
                        ∂²𝝭∂y²[i] += ∂𝝭∂x[i]*W₂₂₁ + ∂𝝭∂y[i]*W₂₂₂
                    end
                end
            end
        end

        for c in Γᴾ
            if ap∩c ≠ nothing
                ξ = c.𝓖[1]
                Δn₁s₁ = ξ.Δn₁s₁
                Δn₁s₂n₂s₁ = ξ.Δn₁s₂n₂s₁
                Δn₂s₂ = ξ.Δn₂s₂

                𝝭 = ξ[:𝝭]
                𝒒 = get𝒑₂(c,ξ)

                𝒒̂ᵀ𝗚⁻¹𝒒 =  𝒒̂ᵀ𝗚⁻¹*𝒒

                Δms₁₁ =  𝒒̂ᵀ𝗚⁻¹𝒒*Δn₁s₁
                Δms₂₂ =  𝒒̂ᵀ𝗚⁻¹𝒒*Δn₂s₂
                Δms₁₂ =  𝒒̂ᵀ𝗚⁻¹𝒒*Δn₁s₂n₂s₁/2

                for i in 1:length(𝓒)
                    ∂²𝝭∂x²[i] += 𝝭[i]*Δms₁₁
                    ∂²𝝭∂x∂y[i] += 𝝭[i]*Δms₁₂
                    ∂²𝝭∂y²[i] += 𝝭[i]*Δms₂₂
                end
            end
        end
    end
