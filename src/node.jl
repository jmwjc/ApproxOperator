@inline getproperty(p::T,f::Symbol) where T<:AbstractNode = hasfield(T,f) ? getfield(p,f) : getfield(p,:data)[f][getfield(p,:id)]
@inline function setproperty!(p::T,f::Symbol,x::Float64) where T<:AbstractNode
    getfield(p,:data)[f][getfield(p,:id)] = x
end

@inline -(n::T,x::NTuple{3,Float64}) where T<:AbstractNode = (n.x-x[1],n.y-x[2],n.z-x[3])
@inline -(x::NTuple{3,Float64},n::T) where T<:AbstractNode = (x[1]-n.x,x[2]-n.y,x[3]-n.z)
@inline +(n::T,x::NTuple{3,Float64}) where T<:AbstractNode = (n.x+x[1],n.y+x[2],n.z+x[3])
@inline +(x::NTuple{3,Float64},n::T) where T<:AbstractNode = (x[1]+n.x,x[2]+n.y,x[3]+n.z)

# ----------------- Node ------------------
struct Node<:AbstractNode
    id::Int
    data::Dict{Symbol,Vector{Float64}}
end

## Meshfree module
# ----------------- MFNode ------------------
struct SNode<:AbstractNode
    id::Int
    data::Dict{Symbol,Vector{Float64}}
    index::Vector{Int}
    𝝭::Dict{Symbol,Vector{Float64}}
end
Node(ξ::SNode) = Node(ξ.id,ξ.data)

function get𝝭(ap::ReproducingKernel{SNode},ξ::SNode)
    𝝭 = ap.𝝭[:∂1]
    i = ξ.id
    index = ξ.index
    for j in 1:length(ap.𝓒)
        𝝭[j] = ξ.𝝭[:∂1][index[i]+j]
    end
    return 𝝭
end

function get∂𝝭∂x(ap::ReproducingKernel{SNode},ξ::SNode)
    ∂𝝭∂x = ap.𝝭[:∂x]
    i = ξ.id
    index = ξ.index
    for j in 1:length(ap.𝓒)
        ∂𝝭∂x[j] = ξ.𝝭[:∂x][index[i]+j]
    end
    return ∂𝝭∂x
end

function get∇𝝭(ap::ReproducingKernel,ξ::SNode)
    𝝭 = ap.𝝭[:∂1]
    ∂𝝭∂x = ap.𝝭[:∂x]
    ∂𝝭∂y = ap.𝝭[:∂y]
    ∂𝝭∂z = ap.𝝭[:∂z]
    i = ξ.id
    index = ξ.index
    for j in 1:length(ap.𝓒)
        𝝭[j] = ξ.𝝭[:∂1][index[i]+j]
        ∂𝝭∂x[j] = ξ.𝝭[:∂x][index[i]+j]
        ∂𝝭∂y[j] = ξ.𝝭[:∂y][index[i]+j]
        ∂𝝭∂z[j] = ξ.𝝭[:∂z][index[i]+j]
    end
    return 𝝭, ∂𝝭∂x, ∂𝝭∂y, ∂𝝭∂z
end

## Quadrature Points
function set𝓖!(aps::Vector{T},s::Symbol) where T<:Approximator
    rule = QuadratureRule[s]
    return set𝓖!(aps,rule)
end
function set𝓖!(aps::Vector{T},s::Symbol,stype::Symbol...;isrk::Bool=false) where T<:ReproducingKernel
    rule = QuadratureRule[s]
    return set𝓖!(aps,rule,stype...;isrk=isrk)
end

function set𝓖!(aps::Vector{T},𝓖::NTuple{N,NTuple{D,Float64}}) where {T<:Approximator,N,D}
    nₑ = length(aps)
    nᵢ = nₑ*N
    data = Dict(:w=>zeros(nᵢ))
    data[:ξ] = zeros(nᵢ)
    N ≥ 3 ? data[:η] = zeros(nᵢ) : nothing
    N ≥ 4 ? data[:γ] = zeros(nᵢ) : nothing

    for ap in aps
        n = 0
        for ξ in 𝓖
            n += 1
            push!(ap.𝓖,Node(n,data))
            set𝓖(n,data,ξ)
        end
    end
end

function set𝓖!(aps::Vector{ReproducingKernel{Node}},𝓖::NTuple{N,NTuple{D,Float64}},stype::Symbol...;isrk::Bool=false) where {N,D}
    nₑ = length(aps)
    nᵢ = nₑ*N
    data = Dict(:w=>zeros(nᵢ))
    data[:ξ] = zeros(nᵢ)
    N ≥ 3 ? data[:η] = zeros(nᵢ) : nothing
    N ≥ 4 ? data[:γ] = zeros(nᵢ) : nothing

    nₘ = 0
    nₕ = length(get𝒑(aps[1],(0.0,0.0,0.0)))
    for ap in aps
        nₘ = max(nₘ,length(ap.𝓒))
    end
    for s in stype
        aps[1].𝗠[s]=SymMat(nₕ)
        if haskey(aps[1].𝝭,s)
            if nₘ>length(aps[1].𝝭[s])
                aps[1].𝝭[s]=zeros(nₘ)
            end
        else
            aps[1].𝝭[s]=zeros(nₘ)
        end
    end
    for ap in aps
        n = 0
        for ξ in 𝓖
            n += 1
            push!(ap.𝓖,Node(n,data))
            set𝓖(n,data,ξ)
        end
    end
end

function set𝓖!(aps::Vector{ReproducingKernel{SNode}},𝓖::NTuple{N,NTuple{D,Float64}},stype::Symbol...;isrk::Bool=false) where {N,D}
    nₑ = length(aps)
    nᵢ = nₑ*N
    data = Dict(:w=>zeros(nᵢ))
    data[:ξ] = zeros(nᵢ)
    N ≥ 3 ? data[:η] = zeros(nᵢ) : nothing
    N ≥ 4 ? data[:γ] = zeros(nᵢ) : nothing

    n = 0
    nₘ = 0
    nₕ = length(get𝒑(aps[1],(0.0,0.0,0.0)))
    index = zeros(Int,nᵢ)
    𝝭 = Dict{Symbol,Vector{Float64}}()
    for ap in aps
        n += length(ap.𝓒)*N
        nₘ = max(nₘ,length(ap.𝓒))
    end
    for s in stype
        push!(𝝭,s=>zeros(n))
        aps[1].𝗠[s]=SymMat(nₕ)
        if haskey(aps[1].𝝭,s)
            if nₘ>length(aps[1].𝝭[s])
                aps[1].𝝭[s]=zeros(nₘ)
            end
        else
            aps[1].𝝭[s]=zeros(nₘ)
        end
    end
    nₜ = 0
    for ap in aps
        n = 0
        for ξ in 𝓖
            n += 1
            index[n] = nₜ
            nₜ += length(ap.𝓒)
            push!(ap.𝓖,SNode(n,data,index,𝝭))
            set𝓖(n,data,ξ,isrk)
        end
    end
end

function set𝓖(i::Int,data::Dict{Symbol,Vector{Float64}},ξ::NTuple{2,Float64},isrk::Bool=false)
    data[:w][i] = ξ[1]
    data[:ξ][i] = ξ[2]
end
function set𝓖(i::Int,data::Dict{Symbol,Vector{Float64}},ξ::NTuple{3,Float64},isrk::Bool=false)
    data[:w][i] = ξ[1]
    data[:ξ][i] = ξ[2]
    isrk ? data[:wᵇ][i] = ξ[3] : data[:η][i] = ξ[3]
end
function set𝓖(i::Int,data::Dict{Symbol,Vector{Float64}},ξ::NTuple{4,Float64},isrk::Bool=false)
    data[:w][i] = ξ[1]
    data[:ξ][i] = ξ[2]
    data[:η][i] = ξ[3]
    isrk ? data[:wᵇ][i] = ξ[4] : data[:γ][i] = ξ[4]
end
function set𝓖(i::Int,data::Dict{Symbol,Vector{Float64}},ξ::NTuple{5,Float64},isrk::Bool=false)
    data[:w][i] = ξ[1]
    data[:ξ][i] = ξ[2]
    data[:η][i] = ξ[3]
    data[:γ][i] = ξ[4]
    data[:wᵇ][i] = ξ[5]
end

